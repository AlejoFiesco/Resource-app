package com.example.demo.model.subclasses;

public class Score {
	private int timeComplied;
	private int resourceQuality;
	private int treatment;
	
	public int getTimeComplied() {
		return timeComplied;
	}
	public void setTimeComplied(int timeComplied) {
		this.timeComplied = timeComplied;
	}
	public int getResourceQuality() {
		return resourceQuality;
	}
	public void setResourceQuality(int resourceQuality) {
		this.resourceQuality = resourceQuality;
	}
	public int getTreatment() {
		return treatment;
	}
	public void setTreatment(int treatment) {
		this.treatment = treatment;
	}
}
